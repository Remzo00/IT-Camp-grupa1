//2. Napisati funkciju za pronalazenje povrsine datog pravougaonika

const pronadjiKvadrat = (broj) => {
  return broj * broj;
};
console.log(pronadjiKvadrat(2));
console.log(pronadjiKvadrat(9));
console.log(pronadjiKvadrat(15));

const povrsinaPravougaonika = (a, b) => {
  return a * b;
};
console.log(povrsinaPravougaonika(10, 20));
